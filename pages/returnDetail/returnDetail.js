  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function(options) {
    var that = this;
    var a = parseFloat(options.back_id.split('"')[1]);
    var url = app.globalData.url + "/api/crmcustomerback/getById";
    wx.request({
      url: url,
      method: 'post',
      data: {
        back_id: a
      },
      success(res) {
        console.log(res)
        that.setData({
          return: res.data.content
        })
      }
    })
  },

